package org.kotrix.rational

fun main() {
    println(Rational(10, 20))
    println(Rational(1, 0))
    println(Rational(-1, 0))
    println(Rational(0, 0))
    println(Rational(20, 20))
    println(Rational(-20, 20))
    println(Rational(20, -20))
    println(Rational(-10, 20))
    println(Rational(10, -20))
    println(Rational(0.3))
    println(Rational("0.3"))
    println(Rational(-1.5))
    println(Rational("-1.5"))
}